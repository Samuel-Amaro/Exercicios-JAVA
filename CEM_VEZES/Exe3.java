package Exercicios.CemVezes;

public class Exe3 {
    public static void main(String[] args) {
        
        for(int i = 0; i < 100; i++) {
            System.out.println("Samuel");
        }

        int j = 0;

        while(j < 100) {
            System.out.println("Samuel");
            j++;            
        }

        int k = 0;

        do{
            k++;
            System.out.println("Samuel");
        }while(k < 100);

    }
}
